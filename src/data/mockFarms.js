// Helper function to get farms by purchase status
export const getPurchasedFarms = () => mockFarms.filter(farm => farm.isPurchased);
export const getAvailableFarms = () => mockFarms.filter(farm => !farm.isPurchased);

// Helper function to get farms by category
export const getFarmsByCategory = (category) => mockFarms.filter(farm => farm.category === category);

// Helper function to toggle purchase status
export const toggleFarmPurchase = (farmId) => {
  const farm = mockFarms.find(f => f.id === farmId);
  if (farm) {
    farm.isPurchased = !farm.isPurchased;
  }
  return farm;
};